package org.psicover.altimeter.ui;

import java.io.IOException;

public class AltimeterIOException extends IOException {

	private static final long serialVersionUID = 6818041051089074978L;

	public AltimeterIOException() {
		super();
	}

	public AltimeterIOException(String message, Throwable cause) {
		super(message, cause);
	}

	public AltimeterIOException(String message) {
		super(message);
	}

	public AltimeterIOException(Throwable cause) {
		super(cause);
	}
	
}
